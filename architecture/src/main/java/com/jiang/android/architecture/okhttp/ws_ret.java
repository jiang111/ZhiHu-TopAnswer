package com.jiang.android.architecture.okhttp;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

/**
 * 网络请求失败或者没数据的时候，调用的类
 * Created by jiang on 15/10/16.
 */
public class ws_ret {

    @SerializedName("status")
    @Expose
    private int state;
    @Expose
    private String msg;

    public ws_ret() {
    }

    public ws_ret(int state, String msg) {
        this.state = state;
        this.msg = msg;
    }


    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getState() {
        return state;
    }

    public void setState(int state) {
        this.state = state;
    }

    @Override
    public String toString() {
        return "ws_ret{" +
                "state=" + state +
                ", msg='" + msg + '\'' +
                '}';
    }
}
